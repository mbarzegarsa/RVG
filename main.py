    body = await request.json()
    link_id = str(body.get("link_id", ""))
    action = str(body.get("action", "add"))  # add | remove
    async with SUBS_LOCK:
        if sub_id not in SUBS:
            raise HTTPException(status_code=404, detail="sub not found")
